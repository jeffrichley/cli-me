class TestConvertErrors:
    def test_missing_input_exits_one_with_clear_message(self, runner, app, tmp_path):
        bogus = tmp_path / "nope.md"
        out = tmp_path / "out.html"
        result = runner.invoke(
            app,
            ["convert", "to", str(bogus), str(out)],
        )
        assert result.exit_code == 1
        # Error appears either on stderr (preferred) or merged output.
        combined = (result.stderr or "") + (result.output or "")
        assert "Input file not found" in combined
        assert not out.exists()

    def test_pdf_with_no_engine_exits_one(
        self, runner, app, simple_md, tmp_path, monkeypatch
    ):
        """Force find_pdf_engines to return [] and verify clean exit + message,
        without attempting any LaTeX run."""
        from pandoc_cli.commands import convert_to

        monkeypatch.setattr(convert_to, "find_pdf_engines", lambda: [])
        out = tmp_path / "out.pdf"
        result = runner.invoke(
            app,
            ["convert", "to", str(simple_md), str(out)],
        )
        assert result.exit_code == 1
        combined = (result.stderr or "") + (result.output or "")
        assert "PDF" in combined
        assert "engine" in combined.lower()


