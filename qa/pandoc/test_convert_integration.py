class TestConvertMetadataPriority:
    def test_cli_metadata_overrides_frontmatter_title(
        self, runner, app, md_with_frontmatter, tmp_path
    ):
        """``--metadata title=Override`` must win over the YAML frontmatter
        ``title: Sample Document`` and end up as the ACTIVE <title> element —
        not just appear somewhere in the document body or metadata block."""
        import re

        out = tmp_path / "out.html"
        result = runner.invoke(
            app,
            [
                "convert",
                "to",
                str(md_with_frontmatter),
                str(out),
                "--standalone",
                "--metadata",
                "title=Override",
            ],
        )
        assert result.exit_code == 0, result.output
        assert_html_doctype(out)
        text = out.read_text(encoding="utf-8")

        # The CLI override must be the actual <title>...</title> element.
        match = re.search(r"<title>(.*?)</title>", text, flags=re.DOTALL)
        assert match is not None, "no <title> element in output HTML"
        title_text = match.group(1).strip()
        assert "Override" in title_text, (
            f"CLI override should be inside <title>; got <title>{title_text!r}</title>"
        )
        # And the frontmatter value must NOT be the active <title>.
        assert "Sample Document" not in title_text, (
            "frontmatter title leaked into <title>; CLI --metadata did not win"
        )
