pytestmark = pytest.mark.integration


@pytest.fixture
def runner() -> CliRunner:
    # mix_stderr defaults to True in older Click versions, False in newer ones.
    # We want stderr separate so we can assert on error messages without
    # polluting stdout assertions.
    try:
        return CliRunner(mix_stderr=False)
    except TypeError:
        # Newer Click drops the kwarg (stderr is always separate).
        return CliRunner()


@pytest.fixture
def app(pandoc_path):
    """Import the Typer app — depends on pandoc_path so we skip cleanly when
    pandoc isn't installed."""
    from pandoc_cli import app as _app

    return _app


