class TestConvertHtml:
    def test_html_output_has_doctype(self, runner, app, simple_md, tmp_path):
        out = tmp_path / "out.html"
        result = runner.invoke(
            app,
            ["convert", "to", str(simple_md), str(out), "--standalone"],
        )
        assert result.exit_code == 0, result.output
        assert_html_doctype(out)

    def test_no_standalone_emits_fragment(self, runner, app, simple_md, tmp_path):
        """``--no-standalone`` must produce a fragment with NO <!DOCTYPE> or
        <html> wrapper — just the body content."""
        out = tmp_path / "frag.html"
        result = runner.invoke(
            app,
            ["convert", "to", str(simple_md), str(out), "--no-standalone"],
        )
        assert result.exit_code == 0, result.output
        text = out.read_text(encoding="utf-8")
        assert "<!DOCTYPE" not in text and "<!doctype" not in text
        assert "<html" not in text.lower()
        # Body content should still be present.
        assert "<h1" in text


class TestConvertEmbedResources:
    def test_embed_resources_inlines_image_as_data_uri(
        self, runner, app, tmp_path
    ):
        """``--embed-resources`` must inline referenced images as ``data:``
        URIs instead of leaving an external ``<img src="path">`` reference.

        We reference the image with an absolute path so pandoc can find it
        regardless of the subprocess cwd (pandoc resolves relative image
        paths against its own working directory, not the markdown's dir,
        unless ``--resource-path`` is set)."""
        # 1x1 transparent PNG.
        png_bytes = bytes.fromhex(
            "89504e470d0a1a0a0000000d49484452000000010000000108060000001f15c4"
            "890000000d49444154789c63000100000005000100"
            "0d0a2db40000000049454e44ae426082"
        )
        img = tmp_path / "pixel.png"
        img.write_bytes(png_bytes)

        src = tmp_path / "with_img.md"
        # Markdown image syntax with parens needs URL-encoding of spaces; the
        # tmp_path here is generated by pytest and shouldn't contain any.
        src.write_text(
            f"# Pic\n\n![alt]({img.as_posix()})\n",
            encoding="utf-8",
        )
        out = tmp_path / "out.html"

        result = runner.invoke(
            app,
            [
                "convert",
                "to",
                str(src),
                str(out),
                "--standalone",
                "--embed-resources",
            ],
        )
        assert result.exit_code == 0, result.output
        text = out.read_text(encoding="utf-8")
        # Inlined PNG appears as data:image/png;base64,...
        assert "data:image/png;base64," in text, (
            "expected data: URI for embedded PNG; got plain src reference"
        )
        # And the raw filename should NOT appear as the src= value.
        assert f'src="{img.as_posix()}"' not in text


class TestConvertDocx:
    def test_docx_has_zip_header(self, runner, app, simple_md, tmp_path):
        """Verify the output is not just any ZIP (which would also accept
        EPUB/JAR/raw zip) but a real DOCX — the OOXML package always contains
        ``word/document.xml`` at the top of the namelist."""
        import zipfile

        out = tmp_path / "out.docx"
        result = runner.invoke(
            app,
            ["convert", "to", str(simple_md), str(out)],
        )
        assert result.exit_code == 0, result.output
        assert_docx_magic(out)

        with zipfile.ZipFile(out) as zf:
            names = zf.namelist()
        assert "word/document.xml" in names, (
            f"DOCX must contain word/document.xml; namelist was: {names[:10]}"
        )


